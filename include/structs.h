struct QARAircraft
{
public:
	QString flightNumber;
	QString tailNumber;
	inline QByteArray Serialize()
	{
		QByteArray data;
		QDataStream stream(&data, QIODevice::WriteOnly);
		stream << flightNumber << tailNumber;
		return data;
	}
        inline QARAircraft deSerialize(QByteArray &data)
	{
		QDataStream stream(&data, QIODevice::ReadOnly);
		stream >> flightNumber >> tailNumber;
		return *this;
	}
};

